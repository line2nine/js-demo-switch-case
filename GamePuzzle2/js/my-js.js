function changeImg1() {
    let srcImg1 = document.getElementById("myImg1").src;
    if (srcImg1 === "http://localhost:63342/js-demo-switch-case/GamePuzzle/img/eagle.jpg") {
        document.getElementById("myImg1").src = "http://localhost:63342/js-demo-switch-case/GamePuzzle/img/horse.jpg";
    } else if (srcImg1 === "http://localhost:63342/js-demo-switch-case/GamePuzzle/img/horse.jpg") {
        document.getElementById("myImg1").src = "http://localhost:63342/js-demo-switch-case/GamePuzzle/img/kittens.jpg";
    } else if (srcImg1 === "http://localhost:63342/js-demo-switch-case/GamePuzzle/img/kittens.jpg") {
        document.getElementById("myImg1").src = "http://localhost:63342/js-demo-switch-case/GamePuzzle/img/kitty.jpg";
    } else if (srcImg1 === "http://localhost:63342/js-demo-switch-case/GamePuzzle/img/kitty.jpg"){
        document.getElementById("myImg1").src = "http://localhost:63342/js-demo-switch-case/GamePuzzle/img/tiger.jpg";
    } else document.getElementById("myImg1").src = "http://localhost:63342/js-demo-switch-case/GamePuzzle/img/eagle.jpg"
}

